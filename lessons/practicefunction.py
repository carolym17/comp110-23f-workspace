# Expected Code 
def mimic(my_words: str) -> str:
    """Given the string my_words, outputs the same string"""
    return my_words

# Calling it 

# One way to call it 
mimic("Hello!")

# Another way to call it 
print(mimic("Hello!"))

# A thrid way to call it 
my_words: str = "Hello!"
response: str = mimic(my_words)
print(response)
